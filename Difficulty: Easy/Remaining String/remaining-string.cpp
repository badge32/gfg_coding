class Solution {
  public:

    string printString(string s, char ch, int count) {
        // Your code goes here
        int i=0;
        int k=0;
        int n=s.size();
        while(k<count && i<n){
            if(s[i]==ch){
                k++;
            }
            i++;
        }
        string newstr =s.substr(i,n-1);
        return newstr;
    }
};


